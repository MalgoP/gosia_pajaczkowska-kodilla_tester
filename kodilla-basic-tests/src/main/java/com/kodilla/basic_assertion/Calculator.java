package com.kodilla.basic_assertion;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtractNumber(int a, int b) {
        return a - b;
    }

    public int squareNumber(int r) {
        return r * r;
    }

    public double subtractNumber(double a, double b) {
        return a - b;
    }

    public double squareNumber(double r) {
        return r * r;
    }
}
